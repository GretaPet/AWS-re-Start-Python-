myFruitList = ["apple", "banana", "cherry"] #Defining a list
print(myFruitList) 
print(type(myFruitList)) 

print(myFruitList[0]) #Accessing a list by position
print(myFruitList[1])
print(myFruitList[2])

myFruitList[2] = "orange" #Changing the values in a list
print(myFruitList)

myFinalAnswerTuple = ("apple", "banana", "pineapple") #Defining a tuple
print(myFinalAnswerTuple)
print(type(myFinalAnswerTuple))

print(myFinalAnswerTuple[0]) #Accessing a tuple by position
print(myFinalAnswerTuple[1])
print(myFinalAnswerTuple[2])

myFavoriteFruitDictionary = { #Defining a dictionary
  "Akua" : "apple",
  "Saanvi" : "banana",
  "Paulo" : "pineapple"
}

